int	ft_is_prime(int b)
{
	int i;

	i = 1;
	if (b <= 1)
		return (0);
	if (b % 2 == 0 && b != 2)
		return (0);
	while (i < b / 2)
	{
		i = i + 2;
		if (b % i == 0)
			return (0);
	}
	return (1);
}
