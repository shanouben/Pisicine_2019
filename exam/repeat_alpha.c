#include <unistd.h>

int main(int ac, char **av)
{
	int i;
	int j;
	
	i = 0;
	if (ac != 2 || av[1][i] == '\0')
	{
		write(1, "\n",1);
		return (0);
	}
	while (av[1][i] != '\0')
	{
		if (av[1][i] >= 'a' && av[1][i] <= 'z')
			j = 'a';
		else
			j = 'A';

		while (av[1][i] > j) 
			{ 
				write(1, &av[1][i], 1);
				j++;
			}
		//write(1, &av[1][i], 1);
		i++;
	}
	
}

